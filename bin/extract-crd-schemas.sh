#!/usr/bin/env bash
# Extract prometheus-operator CRD OpenAPI v3 schemas into embeddable JSONSchema.
#
# The schemas are the source of truth for validating the prometheus-operator
# Monitors under packages/prometheus-scrapers/ (consumed by mzmon-lib's scrape
# transpiler). Rather than hand-maintain them, we pull them straight out of the
# vendored prometheus-operator-crds chart tarball so they track the upstream CRDs.
#
# Re-run this whenever the vendored chart version changes. Output is checked in.

PROG=$0
# set cwd to repo root
cd "$(dirname "$0")/../" || exit 1
# shellcheck source=tools/shlib/common.sh
source "tools/shlib/common.sh"
set -o errexit -o errtrace -o nounset -o pipefail
_register_traceback

# Where the vendored chart tarball lives and where extracted schemas go.
CRD_CHART_DIR="charts/materialize-monitoring-crds/charts"
OUT_DIR="packages/mzmon-lib/schemas/scrape"

# kind | CRD plural (file stem) | served version | output schema stem
# Keep in sync with the ComponentBlock variants in scrape/transpile.rs.
CRDS=(
    "PodMonitor|podmonitors|v1|podmonitor"
    "ServiceMonitor|servicemonitors|v1|servicemonitor"
    "ScrapeConfig|scrapeconfigs|v1alpha1|scrapeconfig"
)

function _require_progs() {
    local missing=false
    for prog in "$@"; do
        if ! _has_prog "$prog"; then
            _error "required program not found on PATH: $prog"
            missing=true
        fi
    done
    [ "$missing" = false ] || exit 1
}

function _find_tarball() {
    # Resolve the single vendored prometheus-operator-crds-*.tgz tarball.
    local matches=("$CRD_CHART_DIR"/prometheus-operator-crds-*.tgz)
    if [ ! -e "${matches[0]}" ]; then
        _error "no prometheus-operator-crds-*.tgz under $CRD_CHART_DIR"
        _error "run: helm dependency update charts/materialize-monitoring-crds"
        exit 1
    fi
    if [ "${#matches[@]}" -gt 1 ]; then
        _error "multiple crds tarballs found; expected exactly one:"
        printf '  %s\n' "${matches[@]}" >&2
        exit 1
    fi
    echo "${matches[0]}"
}

_require_progs yq tar

TARBALL=$(_find_tarball)
CHART_VERSION=$(basename "$TARBALL" .tgz | sed 's/^prometheus-operator-crds-//')
_info "Extracting CRD schemas from $TARBALL (chart version $CHART_VERSION)"

mkdir -p "$OUT_DIR"

for entry in "${CRDS[@]}"; do
    IFS='|' read -r kind plural version stem <<<"$entry"
    crd_path="prometheus-operator-crds/charts/crds/templates/crd-${plural}.yaml"
    out_file="$OUT_DIR/${stem}.schema.yaml"

    _info "  $kind ($version) -> $out_file"

    # The CRD files are helm templates, but the templating is shallow: a top-level
    # `{{- if .Values... }}` guard, a small annotations injection, and a closing
    # `{{- end -}}`. Dropping every `{{`-bearing line leaves a single valid CRD
    # document, from which we lift the served version's openAPIV3Schema.
    schema=$(
        tar xzfO "$TARBALL" "$crd_path" \
            | sed '/{{/d' \
            | yq ".spec.versions[] | select(.name == \"$version\") | .schema.openAPIV3Schema"
    )

    if [ -z "$schema" ] || [ "$schema" = "null" ]; then
        _error "no openAPIV3Schema for $kind version $version in $crd_path"
        exit 1
    fi

    {
        echo "# GENERATED BY $PROG -- DO NOT EDIT."
        echo "# Source: prometheus-operator-crds chart $CHART_VERSION"
        echo "#         crd-${plural}.yaml, ${kind} ${version} (.spec.versions[].schema.openAPIV3Schema)"
        echo "# Re-generate with: bin/extract-crd-schemas.sh"
        echo "$schema"
    } >"$out_file"
done

_info "Done. Wrote ${#CRDS[@]} schema(s) to $OUT_DIR"
